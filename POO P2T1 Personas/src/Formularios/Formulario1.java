    private void jButtonP1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonP1ActionPerformed
        nombre= this.jTextNombre.getText();
        edad= Integer.parseInt(this.jTextEdad.getText());
        estatura= Double.parseDouble(this.jTextEstatura.getText());
        sexo= this.jComboSexo.getSelectedItem().toString().charAt(0);
        dia= Integer.parseInt(this.jTextDia.getText());
        mes= Integer.parseInt(this.jTextMes.getText());
        año= Integer.parseInt(this.jTextAño.getText());
        
        //Crear el oPer1
        oPer1 = new Persona(nombre, edad, estatura, sexo, dia, mes, año);
        
        //Validar la fecha de nacimiento
        if(oPer1.verificarFecha())
        {
            oPer1.mostrarDatos(jTableMostrar);
            if(oPer1.esMayorDeEdad())
                this.jLabelMayorEdad.setText("ES MAYOR DE EDAD");
            else
                this.jLabelMayorEdad.setText("NO ES MAYOR DE EDAD");
        }
        else
        {
            JOptionPane.showMessageDialog(rootPane, "La fecha de nacimiento es incorrecta, vuelve a escribirla");
            this.jTextDia.setText("");
            this.jTextMes.setText("");
            this.jTextAño.setText("");
        }
        
        
    }//GEN-LAST:event_jButtonP1ActionPerformed

