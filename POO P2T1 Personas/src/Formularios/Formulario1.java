    private void jTextAñoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextAñoFocusLost
        Calendar añoActual = Calendar.getInstance();
        int año = Integer.parseInt(this.jTextAño.getText());
        int edad = Integer.parseInt(this.jTextEdad.getText());
        if(añoActual.get(Calendar.YEAR) != (año+edad))
            JOptionPane.showMessageDialog(rootPane, "El año de nacimiento es incorrecto");
    }//GEN-LAST:event_jTextAñoFocusLost

