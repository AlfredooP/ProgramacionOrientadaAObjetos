    private void jButtonCrearTrapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCrearTrapActionPerformed

        Distancia oDist;
        double x1, y1, x2, y2, x3, y3, x4, y4, x5, y5, x6, y6;
        double altura, baseChica, baseGrande;

        x1 = Double.parseDouble(jSpinnerX1.getValue().toString());
        y1 = Double.parseDouble(jSpinnerY1.getValue().toString());

        x2 = Double.parseDouble(jSpinnerX2.getValue().toString());
        y2 = Double.parseDouble(jSpinnerY2.getValue().toString());

        x3 = Double.parseDouble(jSpinnerX3.getValue().toString());
        y3 = Double.parseDouble(jSpinnerY3.getValue().toString());

        x4 = Double.parseDouble(jSpinnerX4.getValue().toString());
        y4 = Double.parseDouble(jSpinnerY4.getValue().toString());

        x5 = Double.parseDouble(jSpinnerX5.getValue().toString());
        y5 = Double.parseDouble(jSpinnerY5.getValue().toString());

        x6 = Double.parseDouble(jSpinnerX6.getValue().toString());
        y6 = Double.parseDouble(jSpinnerY6.getValue().toString());

        //Crear los objetos distancia para la base
        oDist = new Distancia(x1, y1, x2, y2);
        altura = oDist.distanciaEntreDosPuntos();
        
        oDist = new Distancia(x3, y3, x4, y4);
        baseChica = oDist.distanciaEntreDosPuntos();
        
        oDist = new Distancia(x5, y5, x6, y6);
        baseGrande = oDist.distanciaEntreDosPuntos();

        //Crear el objeto Rectangulo
        oTrap = new Trapecio(altura, baseChica, baseGrande);
        JOptionPane.showMessageDialog(null, "La figura Trapecio ha sido creada exitosamente");

    }//GEN-LAST:event_jButtonCrearTrapActionPerformed

