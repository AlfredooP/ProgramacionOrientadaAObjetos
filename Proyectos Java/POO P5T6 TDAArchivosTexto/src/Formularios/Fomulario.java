    //Métodos para manipular el ArrayList
    public void altaEmpAL(Empleado oEmp){
        oAL.add(oEmp);
    }   
    
    public int buscarEmpAL(int numEmpleado){
        for(int i=0; i<oAL.size(); i++){
            if(oAL.get(i).getNumEmpleado() == numEmpleado)
                return i;
        }
        return -1;
    }
    
    public void modificarEmpAL(int pos, Empleado oEmp){
        oAL.set(pos, oEmp);
    }
    public void eliminarEmpAL(int pos){
        oAL.remove(pos);
    }
    
    public void mostrarEmpALTable(JTable tabla){
        DefaultTableModel modelo = (DefaultTableModel)tabla.getModel();
        modelo.setRowCount(oAL.size());
        
        for(int i=0; i<oAL.size(); i++){
            tabla.setValueAt(oAL.get(i).getNumEmpleado(), i, 0);
            tabla.setValueAt(oAL.get(i).getNombEmpleado(), i, 1);
            tabla.setValueAt(oAL.get(i).getSueldoEmpleado(), i, 2);
            tabla.setValueAt(oAL.get(i).getPuestoEmpleado(), i, 3);
        }
        
    }
    
    //Métodos para manipular el archivo de texto
    public void escribirArchivoTexto(){
        FileWriter oFW = null;
        PrintWriter oPW = null;
        
        try {
            oFW = new FileWriter("G:\\ARCHIVOS\\Programación Orientada a Objetos\\Archivos POO\\P5T6.txt", false);
            oPW = new PrintWriter(oFW);
            
            //Ciclo para leer los datos del ArrayList y guardarlos en el archivo de texto
            for(int i=0; i<oAL.size(); i++){
                oPW.print(oAL.get(i).getNumEmpleado() + "-");
                oPW.print(oAL.get(i).getNombEmpleado() + "-");
                oPW.print(oAL.get(i).getSueldoEmpleado() + "-");
                oPW.println(oAL.get(i).getPuestoEmpleado());
            }
            
            oFW.close();
            oPW.close();
            
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(rootPane, "Error de escritura");
        }
    }    
        
    public void leerArchivoTexto(){
        FileReader oFR = null;
        BufferedReader oBR = null;
            
        try {
            oFR = new FileReader("G:\\ARCHIVOS\\Programación Orientada a Objetos\\Archivos POO\\P5T6.txt");
            oBR = new BufferedReader(oFR);

            String cad = oBR.readLine();
            Empleado oEmp;

            while(cad != null){
                StringTokenizer oST = new StringTokenizer(cad, "-");
                oEmp = new Empleado(Integer.parseInt(oST.nextToken()), oST.nextToken(), Double.parseDouble(oST.nextToken()), oST.nextToken());
                //Guardar el objeto emplpeado en el ArrayList
                this.altaEmpAL(oEmp);
                //leer la siguiente linea del archivo
                cad = oBR.readLine();
            }

            oFR.close();
            oBR.close();

        }catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(rootPane, "Error de lectura");
        }catch(IOException ex){
            JOptionPane.showMessageDialog(rootPane, "Error de lectura");
        }
            
    }
    
    public void guardarArchivoTexto(){
        
        PrintWriter oPW;
        
        try{
            oPW = new PrintWriter(new BufferedWriter(new FileWriter(archivo + ".txt")));
            
            //Ciclo para leer los datos del ArrayList y guardarlos en el archivo de texto
            for(int i=0; i<oAL.size(); i++){
                oPW.print(oAL.get(i).getNumEmpleado()+"-");
                oPW.print(oAL.get(i).getNombEmpleado()+"-");
                oPW.print(oAL.get(i).getSueldoEmpleado()+"-");
                oPW.println(oAL.get(i).getPuestoEmpleado());
            }
            
            oPW.close();
            
        }catch(IOException ex){
            JOptionPane.showMessageDialog(null, "Error de escritura");
        }
    }
    
    
    private void jButtonCambiosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCambiosActionPerformed
        
        int noEmp = Integer.parseInt(this.jTextNoEmpleadoBaja.getText());
        
        //Buscar la pos en el ArrayList
        int pos = this.buscarEmpAL(noEmp);
        
        if(pos != -1){
            oEmp = new Empleado(Integer.parseInt(this.jTextNoEmpleado.getText()), this.jTextNombre.getText(), Double.parseDouble(this.jTextSueldo.getText()), this.jTextPuesto.getText());
            //Modificar el objeto Empleado en el ArrayList
            this.modificarEmpAL(pos, oEmp);
            //Guardar el ArrayList en el archivo
            this.escribirArchivoTexto();
            //Mostrar el ArrayList en la tabla
            this.mostrarEmpALTable(jTable1);
        }
        else
            JOptionPane.showMessageDialog(rootPane, "El empleado no existe");
        
    }//GEN-LAST:event_jButtonCambiosActionPerformed

    private void jButtonAltaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAltaActionPerformed
        
        oEmp = new Empleado(Integer.parseInt(this.jTextNoEmpleado.getText()), this.jTextNombre.getText(), Double.parseDouble(this.jTextSueldo.getText()), this.jTextPuesto.getText());
        //Guardar el objeto Empleado en el ArrayList
        this.altaEmpAL(oEmp);
        //Guardar el ArrayList en el archivo
        this.escribirArchivoTexto();
        //Mostrar el ArrayList en la tabla
        this.mostrarEmpALTable(jTable1);
        
    }//GEN-LAST:event_jButtonAltaActionPerformed

