    private void jButtonBajaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBajaActionPerformed
        
        int noEmp = Integer.parseInt(this.jTextNoEmpleadoBaja.getText());
        
        //Buscar la pos en el ArrayList
        int pos = this.buscarEmpAL(noEmp);
        
        if(pos != -1){
            int res = JOptionPane.showConfirmDialog(rootPane, "Seguro que quieres eliminar al empleado?");
            if(res == 0){
                //Dar de baja al empleado en el ArrayList
                this.eliminarEmpAL(pos);
                //Escribir en el archivo de texto
                this.escribirArchivoTexto();
                //Mostrar el ArrayList en la tabla
                this.mostrarEmpALTable(jTable1);
            }
        }
        else
            JOptionPane.showMessageDialog(null, "El empleado no existe");
        
    }//GEN-LAST:event_jButtonBajaActionPerformed

    private void jButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarActionPerformed
        
        jfch = new JFileChooser();
        
        if(jfch.showSaveDialog(this) == JFileChooser.APPROVE_OPTION){
            archivo = jfch.getSelectedFile();
            this.guardarArchivoTexto();
        }
        this.dispose();
        
    }//GEN-LAST:event_jButtonGuardarActionPerformed

