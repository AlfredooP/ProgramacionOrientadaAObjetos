        this.jLabelO3.setText("Nombre:"+oCE3.getNombre()+" / Puesto: "+oCE3.getPuesto()+" / Sueldo: "+oCE3.getSueldo()+" / Depto: "+oCE3.getNombreDepartamento()+" / Num de empleados: "+oCE3.getNumeroEmpleados());
        
        //Mostrar el sueldo que obtuvo en el mes
        this.jLabelSueldo1.setText(oCE1.calcularSueldo(2)+"");
        this.jLabelSueldo2.setText(oCE1.calcularSueldo(23)+"");
        this.jLabelSueldo3.setText(oCE1.calcularSueldo(0)+"");
        
        //Determinar cual de los objetos tiene mayor sueldo
