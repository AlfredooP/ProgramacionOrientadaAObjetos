        //CREAR 3 OBJETOS DEL TIPO CalculosEmpleados
        CalculosEmpleados oCE1 = new CalculosEmpleados("Juan", "Gerente", 23450, "Administración", 17);
        CalculosEmpleados oCE2 = new CalculosEmpleados("Marcelo", "Secretario", 15600, "Dirección", 22);
