        //Solucion
        double mayor = 0.0;
        if(oCE1.getSueldo() > mayor){
            mayor = oCE1.getSueldo();
            if(oCE2.getSueldo() > mayor){
                mayor = oCE2.getSueldo();
                if(oCE3.getSueldo() > mayor){
                    mayor = oCE3.getSueldo();
                }
            }
            else{
                if(oCE3.getSueldo() > mayor){
                  mayor = oCE3.getSueldo();  
                }
            }
        }
        
        JOptionPane.showMessageDialog(null, "El sueldo mayor es " + mayor);
        
        
